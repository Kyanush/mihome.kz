<template>
    <div class="box">

        <div class="box-header with-border">
            <router-link :to="{ path: '/users/create'}" class="btn btn-primary ladda-button">
                            <span class="ladda-label">
                                <i class="fa fa-plus"></i> Создать
                            </span>
            </router-link>
            <input id="filter-search" type="search" class="form-control input-sm pull-right" placeholder="Поиск" v-model="filter.search">
        </div>

        <div class="table-responsive">
            <table class="table table-bordered ">
            <thead>
                <tr>
                    <th></th>
                    <th></th>
                    <th></th>
                    <th></th>
                    <th>
                        <select v-model="filter.role_id" class="form-control">
                            <option value="0">Все</option>
                            <option value="1" v-for="role in roles_list" v-bind:value="role.id">
                                {{ role.description }}
                            </option>
                        </select>
                    </th>
                    <th></th>
                    <th></th>
                </tr>
                <tr>
                    <th>ID</th>
                    <th>Имя</th>
                    <th>Фамилия</th>
                    <th>E-mail</th>
                    <th>Роль</th>
                    <th>Статус</th>
                    <th>Действия</th>
                </tr>
            </thead>
            <tbody>
            <tr class="odd even" v-for="(item, index) in users.data" v-bind:class="{ 'deleted': !item.active }">
                <td>{{ item.id }}</td>
                <td>{{ item.name }}</td>
                <td>{{ item.surname }}</td>
                <td>{{ item.email }}</td>
                <td>{{ item.role.description }}</td>
                <td>
                    <i class="fa" v-bind:class="{'red fa-times-circle' : !item.active, 'green fa-check-circle' : item.active }"></i>
                </td>
                <td>
                    <router-link :to="{ path: '/users/edit/' + item.id}" class="btn btn-xs btn-default">
                        <i class="fa fa-edit"></i> Изменить
                    </router-link>

                    <span class="btn btn-xs btn-default" @click="deleteUser(item, index)" v-if="UserId != item.id">
                         <i class="fa fa-remove"></i> Удалить
                    </span>
                </td>
            </tr>
            </tbody>
            <tfoot>
            <tr>
                <th>ID</th>
                <th>Имя</th>
                <th>Фамилия</th>
                <th>E-mail</th>
                <th>Роль</th>
                <th>Действия</th>
            </tr>
            </tfoot>
        </table>
        </div>

        <div class="text-center">
            <paginate
                    v-if="users.last_page > 1"
                    v-model="users.current_page"
                    :page-count="users.last_page"
                    :click-handler="SetPage"
                    :prev-text="'<<'"
                    :next-text="'>>'"
                    :container-class="'pagination'"
                    :page-class="'page-item'"></paginate>
        </div>


    </div>
</template>


<script>
    import { mapGetters } from 'vuex';
    import Paginate from 'vuejs-paginate';

    export default {
        components:{
            Paginate
        },
        data () {
            return {
                users: [],
                filter:{
                    role_id: (this.$route.query.role_id ? this.$route.query.role_id : 0),
                    page:    (this.$route.query.page    ? this.$route.query.page : 1),
                    search:  (this.$route.query.search  ? this.$route.query.search : '')
                },
                roles_list: []
            }
        },
        methods:{
            SetPage(page){
                this.filter.page = page;
            },
            deleteUser(item, index){
                this.$swal({
                    title: 'Вы действительно хотите удалить "' + item.name + '"?',
                    type: 'warning',
                    showCancelButton: true,
                    confirmButtonColor: '#3085d6',
                    cancelButtonColor: '#d33',
                    confirmButtonText: 'Да',
                    cancelButtonText: 'Нет'
                }).then((result) => {
                    if (result.value) {
                        axios.post('/admin/user-delete/' + item.id).then((res)=>{
                            if(res.data)
                            {
                                this.$helper.swalSuccess('Успешно удален');
                                this.$delete(this.users.data, index);
                            }
                        });
                    }
                });
            },
            usersList(){
                var params = {};
                if(this.filter.page > 1 && !this.filter.search)
                    params['page'] = this.filter.page;

                if(this.filter.search)
                    params['search'] = this.filter.search;

                if(this.filter.role_id > 0)
                    params['role_id'] = this.filter.role_id;

                axios.get('/admin/users-list', {params:  params}).then((res)=>{
                    this.users = res.data;
                    this.$router.push({query: params});
                });
            },
        },

        watch: {
            filter: {
                handler: function (val, oldVal) {
                    this.usersList();
                },
                deep: true
            }
        },
        created(){
            this.usersList();

            axios.get('/admin/roles-list').then((res)=>{
                this.roles_list =  res.data;
            });
        },
        computed:{
            ...mapGetters([
                'UserId'
            ]),
        }

    }
</script>